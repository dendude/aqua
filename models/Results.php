<?php

namespace app\models;

use app\components\simple_html_dom;
use app\helpers\Normalize;
use Yii;
use yii\helpers\Url;

/**
 * This is the model class for table "{{%Results}}".
 *
 * @property integer $id
 * @property integer $manager_id
 * @property integer $id_type
 * @property string $menu_title
 * @property string $photo
 * @property integer $photo_x1
 * @property integer $photo_y1
 * @property integer $photo_x2
 * @property integer $photo_y2
 * @property string $title
 * @property string $alias
 * @property string $about
 * @property string $content
 * @property string $meta_t
 * @property string $meta_k
 * @property string $meta_d
 * @property integer $created
 * @property integer $modified
 * @property integer $views
 * @property integer $ordering
 * @property integer $status
 */
class Results extends \yii\db\ActiveRecord
{
    const ALIAS_PREFIX = '/result/';
    const PAGE_ID = 138;

    /**
     * @inheritdoc
     */
    public static function tableName()
    {
        return '{{%results}}';
    }

    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['manager_id', 'section_id', 'menu_title', 'title', 'alias', 'about', 'content'], 'required'],

            [['alias'], 'unique', 'message' => 'Такая ссылка уже занята'],
            
            [['manager_id', 'section_id', 'created', 'modified', 'views', 'ordering', 'status', 'is_slider'], 'integer'],
            [['manager_id', 'section_id', 'created', 'modified', 'views', 'ordering', 'status', 'is_slider'], 'default', 'value' => 0],
            
            [['about', 'content'], 'string'],
            [['photo'], 'string', 'max' => 50],
            [['alias'], 'string', 'max' => 200],
            [['menu_title', 'title'], 'string', 'max' => 150],
            [['meta_t', 'meta_k', 'meta_d'], 'string', 'max' => 250],

            [['menu_title', 'title', 'meta_t', 'meta_k', 'meta_d', 'photo'], 'default', 'value' => ''],
        ];
    }

    public function getManager() {
        return $this->hasOne(Users::className(), ['id' => 'manager_id']);
    }

    public function getSection() {
        return $this->hasOne(ResultsSections::className(), ['id' => 'section_id']);
    }

    public function getBreadcrumbs() {

        $breadcrumbs = [];

        $results = Pages::findOne(self::PAGE_ID);

        $breadcrumbs[] = ['url' => Url::to([Normalize::fixAlias($results->alias)]),
                        'label' => $results->crumb];

        $breadcrumbs[] = ['url' => Url::to([$results->alias, 'section' => $this->section->id]),
                        'label' => $this->section->name];

        $breadcrumbs[] = ['label' => $this->title];

        return $breadcrumbs;
    }

    public function getFixLinksContent() {

        if (!empty($this->content)) {
            // преобразование ссылок в контенте, вдруг сохранены неправильно
            $dom = new simple_html_dom();
            $dom->load($this->content);
            foreach ($dom->find('a') AS $a) {
                // убираем суффикс
                $a->href = trim(str_replace(Yii::$app->urlManager->suffix, '', $a->href), '/');
                // получаем полную ссылку
                $a->href = Url::to([Normalize::fixAlias($a->href)]);
            }

            $this->content = $dom->outertext;
        }

        return $this->content;
    }

    public function getFirstImage() {

        $image = '';

        if (!empty($this->content)) {
            // преобразование ссылок в контенте, вдруг сохранены неправильно
            $dom = new simple_html_dom();
            $dom->load($this->content);
            if ($dom->find('img')) {
                $image = $dom->find('img',0)->src;
                if (strpos($image, 'http') === false) {
                    $image = Yii::$app->request->hostInfo . $image;
                }
            }
        }

        return $image;
    }

    public function beforeValidate()
    {
        $this->manager_id = Yii::$app->user->id;

        if ($this->isNewRecord) {
            $this->created = time();
        } else {
            $this->modified = time();
        }

        return parent::beforeValidate(); // TODO: Change the autogenerated stub
    }


    /**
     * @inheritdoc
     */
    public function attributeLabels()
    {
        return [
            'id' => 'ID',
            'manager_id' => 'Менеджер',
            'section_id' => 'Раздел',
            'menu_title' => 'Название в списке',
            'photo' => 'Фото',
            'title' => 'Заголовок (Н1)',
            'alias' => 'Alias (URL)',
            'about' => 'Краткое описание',
            'content' => 'Содержание',
            'meta_t' => 'Meta Title',
            'meta_k' => 'Meta Keywords',
            'meta_d' => 'Meta Description',
            'created' => 'Создан',
            'modified' => 'Изменен',
            'views' => 'Просмотров',
            'ordering' => 'Порядок',
            'status' => 'Статус',
            'is_slider' => 'Слайдер',
        ];
    }
}
