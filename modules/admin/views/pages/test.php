<p>Данное направление предполагает индивидуальный подход к заказу. С каждым из этих материалов работает отдельный контингент сотрудников: проектировщиков, художников, мастеров.</p>
<p>Приобретенные технологии, использование нашего базового производства аквариумов и домашних водоемов, привлечение наших специалистов по фитодизайну, позволяют осуществлять комплексное оформление помещений. Изделия из полимербетона и стадонта применяются в ландшафтном дизайне.</p>
