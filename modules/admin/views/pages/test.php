<p>С тех пор, мы выполняем отдельные заказы и изготавливаем небольшие партии изделий оригинальных напольных, настенных и настольных конструкций. Развернутое производство аквариумов позволило быстро освоить изготовление панелей из пластиков, силикатного и оргстекла, реализовывать проекты с применением искусственных декораций.</p>
<p>Фирма обладает производственными мощностями и опытом организации передержки и подготовки рыб и растений к высадке в аквариумы заказчиков. Наша база создана с учётом не только изготовления водоёмов, но и наполнения их содержимым и обеспечения профилактического обслуживания. Для этого мы располагаем большим номенклатурным фондом, достаточным для разнообразного оформления внутреннего мира аквариумов, и возможностью его систематического пополнения с учётом интересов заказчиков.</p>