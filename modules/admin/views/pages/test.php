<p>В настоящее время мы вышли на уровень изготовления различных моделей из силикатного и акрилового стекла, с тумбами и крышками при их общем объёме под воду до 15000 литров в месяц.<br /> Наше производство не накладывает ограничения на размерный ряд изделий, изготавливаемых как серийно, так и на заказ. Нашей продукцией являются как небольшие, так и многотонные аквариумы. Применяемые методы позволяют реализовывать сложные проекты, в том числе со сборкой и отделкой крупномерных водоемов на месте.</p>
<p>Имея основательную, развитую производственную базу, мы быстро перестраиваемся под требования современного рынка. Наряду с растущим спросом на стандартные модели с различной отделкой, постоянно увеличивается доля индивидуальных заказов со встроенными аквариумами, необычными конструкциями, изделиями, комбинированными с водопадами, декорациями из искусственного камня.</p>
<p>Для аквариумной мебели, мы предлагаем большой выбор отделочных материалов: от ламинированного ДСП до массива ценных пород дерева. На нашем производстве, мы изготавливаем аквариумы, соответствующие любому интерьеру.</p>
<p><img class="pull-left" src="/images/konyok3.gif" alt="" width="25" height="50" />Увеличение спроса на применение искусственного камня для оформления аквариумов, создание природных интерьеров в помещениях, указало на необходимость освоения работы с полимерными материалами. Изначально, освоив изготовление искусственного скального ландшафта для аквариумов, мы имеем к настоящему времени развитое производство горок, водопадов, декоративных изделий для оформления помещений и ландшафтного дизайна. Применяемые нами материалы это: стадонт, полимерные смолы, полиуретан, полимербетон.</p>
