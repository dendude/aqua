<p>Мы начинали производство аквариумов и аквариумной мебели на базе небольшой мастерской задолго до образования компании "Альфаро". <br /> После организации фирмы в 2000 году и увеличения потока заказов, небольшая мастерская выросла и преобразовалась в цех с расширенными возможностями изготовления эксклюзивных моделей. За многолетнюю практику нами приобретен опыт и разработаны собственные технологии, поднявшие производство аквариумов и аквариумной мебели на новый уровень. Улучшилась производственная база, появилась возможность выпуска массовых изделий. Для этого были разработаны типовые (или стандартные) варианты, представленные в разделе "<a href="/price.php"><u>Цены на аквариумы</u></a>".</p>
